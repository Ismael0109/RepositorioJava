package Aulas_Joyce;

public class SwitchCase2 {

	public static void main(String[] args) {
		String time = "Palmeiras";
		switch (time) {
		case "Palmeiras":
			
		case "Santos":
			
		case "São Paulo":
			System.out.println("De São Paulo");
			break;
		case "Vasco":
			System.out.println("Do Rio de Janeiro");
		}

	}

}
